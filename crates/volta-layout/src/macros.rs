// 这个宏用于构建路径缓冲区（PathBuf）
// 它接受一个基础路径和任意数量的路径组件作为参数

macro_rules! path_buf {
    // 宏规则：接受一个基础表达式 $base 和零个或多个表达式 $x
    ($base:expr, $( $x:expr ), *) => {
        {
            // 创建一个可变的临时变量，初始化为基础路径
            let mut temp = $base;
            // 对每个额外的路径组件 $x 执行以下操作
            $(
                // 将当前组件添加到路径中
                temp.push($x);
            )*
            // 返回构建好的路径
            temp
        }
    }
}
